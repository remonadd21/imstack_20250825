package com.company.java002_ex;

public class VarEx001 {
	public static void main(String[] args) {
		
		int apple = 1000;
		System.out.println("사과의 가격은 "+apple+"원 입니다.");
		
		// 문제2  apple에 2000을 대입하고, 
		// 사과의 가격은 2000원 입니다. 출력받기
		
		apple=2000;
		System.out.println("사과의 가격은 "+apple+"원 입니다.");
		
		// 문제3 
		/*
		 * com.company.java002_ex
		 * VarEx002
		 *  
			정수형데이터를 담을수 있는 변수 a 만들고   
		 	a에 10대입하시오
		    정수형데이터를 담을수 있는 변수 b 만들고   
		    b에 3대입하시오
		    System.out.println 을 4번사용해서 
		     10 + 3 = 13
		     10  - 3 = 7
		     10  * 3 = 30
		     10  / 3 = 3

		 * */
		
		
	}
}
